#Sum-of-two-numbers
def add_numbers(a, b):
    result = a + b
    return result

#Example 1
print("Example 1")
sum_result = add_numbers(5, 7)
print("The sum of 5 and 7 is" , sum_result,".")

#Example 2
print("\nExample 2")
sum_result = add_numbers(-3, -4)
print("The sum of -3 and -4 is" , sum_result,".")

#Example 3
print("\nExample 3")
sum_result = add_numbers(10, -3)
print("The sum of 10 and -3 is" , sum_result,".")