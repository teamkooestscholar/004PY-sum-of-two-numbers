def add_numbers(a, b):
    return a + b

# Example 1
result1 = add_numbers(5, 7)
print("Input: 5 and 7")  
print("Output:", result1)
print("Explanation: The sum of 5 and 7 is ", result1)

# Example 2
result2 = add_numbers(-3, -4)
print("\nInput: -3 and -4")  
print("Output:", result2)
print("Explanation: The sum of -3 and -4 is ", result2)

# Example 3
result3 = add_numbers(10, -3)
print("\nInput: 10 and -3")  
print("Output:", result3)
print("Explanation: The sum of 10 and -3 is ", result3)